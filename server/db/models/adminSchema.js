import { Schema, model } from "mongoose";

const AdminSchema = Schema({
  username: {
    type: String,
    required: true,
    trim: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
    trim: true,
    unique: true,
  },
  name: {
    type: String,
    required: true,
    trim: true,
  },
});

const Admin = model("Admin", AdminSchema);

export default Admin;
